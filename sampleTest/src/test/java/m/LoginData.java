package m;

public class LoginData {
    private final String imya;
    private final String parol;

    public LoginData(String imya, String parol) {
        this.imya = imya;
        this.parol = parol;
    }

    public String getImya() {
        return imya;
    }

    public String getParol() {
        return parol;
    }
}
